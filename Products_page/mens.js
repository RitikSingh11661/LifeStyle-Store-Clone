function ShowProduct(data) {

  let product = document.getElementById("products");
  product.innerHTML = null;

  data.forEach(function (el, i) {
    let div = document.createElement("div");
    div.setAttribute("class", "product_details");

    let image = document.createElement("img");
    image.src = el.image;

    let price_div = document.createElement("div");
    price_div.setAttribute("class", "price_div");

    let price = document.createElement("h4");
    price.innerText = `â‚¹ ${el.price}`;

    let offer = document.createElement("h5");
    offer.innerText = `${el.offer} OFF`;

    price_div.append(price, offer);

    let size_color_div = document.createElement("div");
    size_color_div.setAttribute("class", "size_color_div");

    let color_div = document.createElement("div");
    color_div.setAttribute("class", "color_div");

    let color_img = document.createElement("img");
    color_img.src = el.image;

    let color = document.createElement("h5");
    color.innerText = el.color

    color_div.append(color_img, color);


    let dropdown = document.createElement("div");
    dropdown.setAttribute("class", "dropdown");
    dropdown.addEventListener("click", function () {
      myFunction();
    })

    let size_div = document.createElement("button");
    size_div.innerText = "Select Size"
    size_div.setAttribute("class", "dropbtn");

    let button_img = document.createElement("img");
    button_img.src = "https://i1.lmsin.net/website_images/static-pages/brand_exp/brand2images/icons/down-arrow-black.svg"

    let dropdown_content = document.createElement("div");
    dropdown_content.setAttribute("class", "dropdown-content");
    dropdown_content.setAttribute("id", "myDropdown");

    let a1 = document.createElement("a");
    a1.innerText = "S";

    let a2 = document.createElement("a");
    a2.innerText = "M";

    let a3 = document.createElement("a");
    a3.innerText = "L";

    let a4 = document.createElement("a");
    a4.innerText = "XL";

    dropdown_content.append(a1, a2, a3, a4);

    size_div.append(button_img);

    dropdown.append(size_div ,dropdown_content);

    size_color_div.append(color_div, dropdown);

    let title = document.createElement("p");
    title.innerText = el.title;

    let add_to_cart = document.createElement("button");
    add_to_cart.innerText = "Add To Basket";
    add_to_cart.setAttribute("class", "add_to_cart");
    add_to_cart.addEventListener("click", function () {
      addToCart(el);
    })

    div.append(image, price_div, title, size_color_div, add_to_cart);

    product.append(div);
  });
}

function myFunction() {
  // console.log('i: ', i);
  document.getElementById("myDropdown").classList.toggle("show");
}

// Close the dropdown menu if the user clicks outside of it
window.onclick = function (event) {
  if (!event.target.matches('.dropbtn')) {
    var dropdowns = document.getElementsByClassName("dropdown-content");
    var i;
    for (i = 0; i < dropdowns.length; i++) {
      var openDropdown = dropdowns[i];
      if (openDropdown.classList.contains('show')) {
        openDropdown.classList.remove('show');
      }
    }
  }
}



function getoptions(){
  let sorted = document.getElementById("sorted").value;
  // console.log('sorted: ', sorted);
  if(sorted === "ls"){
    mens.sort(function(a,b){
      return +a.price - +b.price;
     });
     ShowProduct(mens);
  }
  if(sorted === "hs"){
    mens.sort(function(a,b){
      return +b.price - +a.price;
     });
     ShowProduct(mens);
  }
  if(sorted === "alphabetical"){
    mens.sort(function(a,b){
      return a-b;
     });
     ShowProduct(mens);
  }
}


